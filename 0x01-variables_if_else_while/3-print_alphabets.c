#include <stdio.h>
#include <stdlib.h>

/**
 * main - function to print alphabets in lowercase and uppercase
 * Return: always returns 0
 */

int main(void)
{
	char a = 'a';
	char A = 'A';

	while (a <= 'z')
	{
		putchar(a);
		a++;
	}
	while (A <= 'Z')
	{
		putchar(A);
		A++;
	}
	putchar('\n');
	return (0);
}
